<p> 
    GLPI (acronyme de gestionnaire libre de parc informatique) est un logiciel libre de gestion des services informatiques (ITSM) et de gestion des services d’assistance(issue tracking system et ServiceDesk). Cette solution libre est éditée en PHP et distribuée sous licence GPL. <br>
                                
    En tant que technologie libre, toute personne peut exécuter, modifier ou développer le code qui est libre. De ce fait, les contributeurs peuvent participer à l’évolution du logiciel en soumettant des modules supplémentaires libre et open source, sur github. <br>

    GLPI est une application web qui aide les entreprises à gérer leur système d’information. Parmi ses caractéristiques, cette solution est capable de construire un inventaire de toutes les ressources de la société et de réaliser la gestion des tâches administratives et financières. Les fonctionnalités de cette solution aident les Administrateurs IT à créer une base de données regroupant des ressources techniques et de gestion, ainsi qu’un historique des actions de maintenance. La fonctionnalité de gestion d’assistance ou helpdesk fournit aux utilisateurs un service leur permettant de signaler des incidents ou de créer des demandes basées sur un actif ou non, ceci par la création d’un ticket d’assistance qui est géré par un technicien . <br><br>
    Les Fonctionnalités de GLPI :

    Inventaire des ordinateurs, périphériques, réseau, imprimantes et consommables associés. Notamment grâce à un interfaçage avec OCS Inventory NG

    Gestion des licences (acquises, à acquérir, sites, oem..) et des dates d’expiration.<br>
    Affectation du matériel par zone géographique (salle, étage…), par groupes d’utilisateurs et par utilisateurs. <br>
    Gestion des informations commerciales et financières (achat, garantie et extension, amortissement). <br>
    Gestion des états de matériel. <br>
    <br>
    Gestion des demandes d’intervention pour tous les types de matériel de l’inventaire (helpdesk). <br>
    Gestion du planning et export au format Ical pour Assistant personnel <br>
    Interface pour permettre à l’utilisateur final de déposer une demande d’intervention. <br>
    Gestion des entreprises, contrats, documents liés aux éléments d’inventaires… <br>
    Gestion des réservations de matériel <br>
    Gestion d’un système de base de connaissances hiérarchique (FAQ) , gestion d’une FAQ publique. <br>
    Génération de rapports sur le matériel, de rapports réseau, de rapports sur les interventions. <br>
    Télédéploiement, avec l’utilisation conjointe d’OCS Inventory NG <br>
    Interface paramétrable
    Ajout aisé de fonctionnalité grâce à un système de plugins
    Communication avec des annuaires existants
