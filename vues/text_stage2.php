<p>
    L'entreprise TF est une entreprise unipersonnelle de service en partenariat avec plusieurs entreprises de location de véhicule.
    <br>
    Sa mission est la livraison de véhicules pour des particuliers ou des entreprises.
    <br><br>
    L'activité est simple mais la majorité des livraisons ont un point de départ et un point d'arrivée différents à chaque fois.
    <br>
    Pour faciliter l'activité, un site existe pour les entreprises dont le rôle est le même que l'entreprise TF.
    <br>
    Mais ce site ne permet pas de collaboration et c'est ce que souhaite TF, elle collabore déjà avec des entreprises telles que Hertz.
    <br>
    Pour cela, TF a décidé de créer un site permettant à ces entreprises de déposer une livraison à effectuer. 
    <br>
    
