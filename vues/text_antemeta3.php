<p>
Durant mon stage au sein de cette entreprise le responsable du service recherche et développement, Frédéric Chaverot m’a confié la tâche de <em> réorganisation des classes des tickets </em> au sein du service. <br><br>
Il m’a remis un fichier CSV de 200Mo contenant les tickets triés sur d’anciens critéres trop généraux. <br><br>
Pour réaliser ce projet, j’ai créé un logiciel permettant à l’utilisateur de lire le fichier CSV à travers un tableau, rendant la lecture plus facile. <br>
Après sélection du ticket à modifier, l’utilisateur aura accès à une liste permettant de reclassifier le ticket dans la catégorie choisie. <br>
Après que les modifications aient été faites, l’utilisateur aura la possibilité d’enregistrer la totalité des données dans un fichier CSV nommé à sa guise. <br>


